#!/bin/bash
#
# LedgerLeap Dependency Installation and Migration Script
#
# This script installs Composer and NPM dependencies and runs database migrations.
# It is intended to be run after the Docker containers are up and the database is ready.
#
# Usage:
# ./bin/install_dependencies_and_migrate.sh
#

# Exit immediately if a command exits with a non-zero status.
set -e

# --- Helper Functions ---
info() {
    echo "INFO: $1"
}

# --- Main Installation ---

info "Starting dependency installation and database migration..."

# 1. Install Composer dependencies
info "Installing Composer dependencies..."
./vendor/bin/sail composer install

# 2. Generate application key (if not already generated by setup.sh)
# This is redundant if setup.sh already generates it, but harmless.
info "Generating application key..."
./vendor/bin/sail artisan key:generate

# 3. Run central database migrations
info "Running central database migrations..."
./vendor/bin/sail artisan migrate

# 4. Install NPM dependencies and build assets
info "Installing NPM dependencies..."
./vendor/bin/sail npm install

info "Building front-end assets..."
./vendor/bin/sail npm run build

info "Dependency installation and migration complete!"
